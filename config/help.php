<a name="toc"><p><font size="3"><b>Table of Contents</b></font></p>
Layers in the table of contents can be toggled on and off.  Click and drag to rearrange the layer order.  Right click on a layer to change the opacity, get more information, or perform a quick data filter (e.g. select a trail use type, land class or fish species.)</br>
