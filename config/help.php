<a name="download"></a><p><font size="4"><b>Downloads and Web Map Services</b></font></p>
There are several ways to bring data from ARGIS to your local computer:</br>
</br>
1. All layers are available for download in shapefile, kml or kmz format in the "Download Center" tab.</br>
</br>
2. If you only need data for a portion of the layer extent, click on our <img src="img/subset.png"/> tool to zoom in and extract the data you want.</br>
</br>
3. If would like to view the layers in your own mapping application (e.g. ArcGIS, Google Earth or uDig), but do not need/want to download the raw data, you can use the following URL to add the <span style="color:red"><b>Adirondack Web Map Service</b></span> to your application:</br></br>
<b><u><span style="color:blue"> http://aprgis.org:8080/geoserver/ows?service=wms&version=1.1.1&request=GetCapabilities&</span></u></b>
